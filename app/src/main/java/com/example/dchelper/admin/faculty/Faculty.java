package com.example.dchelper.admin.faculty;

public class Faculty {
    private String name;
    private String id;

    public Faculty() {
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    public Faculty(String name, String id) {
        this.name = name;
        this.id = id;
    }
}
